// else if = switch
const znamka = 3

if(znamka === 1) {
    console.log("Vyborny")
} else if(znamka === 2) {
    console.log("Chvalitebny")
} else if(znamka === 3) {
    console.log("Dobry")
} else if(znamka === 4) {
    console.log("Dostatecny")
} else if(znamka === 5) {
    console.log("Nedostatecny")
} else {
    console.log("Spatne zadana hodnota")
}